def fix_industry(name):
    """修正行业名称"""
    if not name:
        return name
    # 先尝试精确匹配
    if name in INDUSTRY_MAP:
        return INDUSTRY_MAP[name]
    # 尝试前缀匹配
    for key, value in INDUSTRY_MAP.items():
        if name.startswith(key[:2]):
            return value
    return name
